public class Switch02 {
    public static void main(String[] args) {
        int val = 2;
        if (val == 3) {
            System.out.println("よくできました！");
        } else if (val == 2) {
            System.out.println("ふつうでした！");
        } else {
            System.out.println("がんばろう！");
        }
    }
}